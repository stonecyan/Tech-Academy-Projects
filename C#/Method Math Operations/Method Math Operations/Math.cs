using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Math_Operations
{
    public class MathOps
    {
        public static double Square(double n)
        {
            double square = n * n;
            return square;
        }

        public static double Multiplybyfive(double n)
        {
            double mult = n * 5;
            return mult;

        }

        public static double Dividebyten(double n)
        {
            double divide = n / 10;
            return divide;
        }
    }
}
